package sec1;

public class NetworkExam1 {

	public static void main(String[] args) {
		System.out.println("Network : 여러 컴퓨터를 통신 회선으로 연결한 망");
		System.out.println("LAN(Local Area Network) : 근 거리 통신망");
		System.out.println("WAN(Wide Area Network) : 광역 통신망, 인터넷");
		
		System.out.println("서버/클라이언트 프로그래밍");
		System.out.println("서버(Server) : 서비스를 공급하는 프로그램 또는 하드웨어");
		System.out.println("클라이언트(Client) : 서비스를 요청하며, 서비스를 받는 프로그램 또는 하드웨어");

		System.out.println("TCP/IP : 인터넷을 연결하기 위한 기본 프로토콜");
		System.out.println("TCP(Transfer Control Protocol) : 전송 프로토콜");
		System.out.println("IP(Internet Protocol) :  인터넷 규약으로 해당 컴퓨터는 반드시 IP주소를 갖는다.");
		
		System.out.println("LAN 카드(Network Adapter) : IP주소를 MAC 주소로 변환");
		System.out.println("도메인 네임(Domain Name) : 해당 서버에 연결하여 서비스를 받고자 할 경우 IP 주소를 외우기 힘들므로 짧게 쉬운 단어로 지정하여 사용");
		System.out.println("URL(Uniform Resource Locator) : 자원의 위치 및 주소를 의미한다.");
		
	}

}
